<?php

$a = 10;
echo $a++; //++ adiciona mais 1 na variavel.

echo "<br>";

echo $a;
echo "<br>";

echo ++$a; //++ antes da variavel adiciona +1 antes da variavel. nesses caso os valores de $a++ e ++$a serao iguais.
echo "<br>";

echo --$a; //tem o mesmo sentido de ++, porém diminui ao invés de somar.


?>